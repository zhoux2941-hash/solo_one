-- 插入历史借还记录（模拟过去2周的数据）
-- 先创建存储过程生成测试数据
DELIMITER //

CREATE PROCEDURE GenerateTestRecords()
BEGIN
    DECLARE i INT DEFAULT 1;
    DECLARE point_id INT;
    DECLARE bike_id INT;
    DECLARE record_type ENUM('BORROW', 'RETURN');
    DECLARE record_time DATETIME;
    DECLARE hour_val INT;
    DECLARE day_offset INT;
    DECLARE min_offset INT;
    
    -- 生成过去14天的记录，每天每小时根据时段生成不同数量的记录
    SET day_offset = 14;
    
    WHILE day_offset > 0 DO
        SET hour_val = 0;
        WHILE hour_val < 24 DO
            -- 每个停车点每小时生成随机记录
            SET point_id = 1;
            WHILE point_id <= 100 DO
                -- 根据时段确定记录数量
                -- 早高峰(7-9): 高借车率
                -- 晚高峰(17-19): 高借车率
                -- 午间(11-13): 中等
                -- 夜间(0-5): 低
                SET min_offset = 0;
                
                -- 根据时段和停车点类型生成记录
                IF (hour_val >= 7 AND hour_val <= 9) OR (hour_val >= 17 AND hour_val <= 19) THEN
                    -- 高峰时段生成较多记录
                    SET i = 1;
                    WHILE i <= FLOOR(2 + RAND() * 3) DO
                        SET bike_id = FLOOR(1000 + RAND() * 5000);
                        SET min_offset = FLOOR(RAND() * 60);
                        SET record_time = DATE_SUB(NOW(), INTERVAL (day_offset * 24 + hour_val) HOUR) 
                                        + INTERVAL min_offset MINUTE;
                        
                        -- 早高峰：商业中心借车多，住宅区还车多
                        -- 晚高峰：商业中心还车多，住宅区借车多
                        IF hour_val >= 7 AND hour_val <= 9 THEN
                            IF point_id IN (1,2,3,4,5,6,9,11,18,19,22,25,31,46,47,55) THEN
                                SET record_type = 'BORROW';
                            ELSE
                                SET record_type = IF(RAND() > 0.3, 'BORROW', 'RETURN');
                            END IF;
                        ELSE
                            IF point_id IN (1,2,3,4,5,6,9,11,18,19,22,25,31,46,47,55) THEN
                                SET record_type = 'RETURN';
                            ELSE
                                SET record_type = IF(RAND() > 0.5, 'BORROW', 'RETURN');
                            END IF;
                        END IF;
                        
                        INSERT INTO bike_record (point_id, bike_id, type, time) 
                        VALUES (point_id, bike_id, record_type, record_time);
                        
                        SET i = i + 1;
                    END WHILE;
                    
                ELSEIF (hour_val >= 11 AND hour_val <= 13) OR (hour_val >= 14 AND hour_val <= 16) THEN
                    -- 午间时段中等数量
                    SET i = 1;
                    WHILE i <= FLOOR(1 + RAND() * 2) DO
                        SET bike_id = FLOOR(1000 + RAND() * 5000);
                        SET min_offset = FLOOR(RAND() * 60);
                        SET record_time = DATE_SUB(NOW(), INTERVAL (day_offset * 24 + hour_val) HOUR) 
                                        + INTERVAL min_offset MINUTE;
                        SET record_type = IF(RAND() > 0.5, 'BORROW', 'RETURN');
                        
                        INSERT INTO bike_record (point_id, bike_id, type, time) 
                        VALUES (point_id, bike_id, record_type, record_time);
                        
                        SET i = i + 1;
                    END WHILE;
                    
                ELSEIF hour_val >= 20 AND hour_val <= 23 THEN
                    -- 晚间时段少量记录
                    SET i = 1;
                    WHILE i <= FLOOR(RAND() * 2) DO
                        SET bike_id = FLOOR(1000 + RAND() * 5000);
                        SET min_offset = FLOOR(RAND() * 60);
                        SET record_time = DATE_SUB(NOW(), INTERVAL (day_offset * 24 + hour_val) HOUR) 
                                        + INTERVAL min_offset MINUTE;
                        SET record_type = IF(RAND() > 0.5, 'BORROW', 'RETURN');
                        
                        INSERT INTO bike_record (point_id, bike_id, type, time) 
                        VALUES (point_id, bike_id, record_type, record_time);
                        
                        SET i = i + 1;
                    END WHILE;
                END IF;
                
                SET point_id = point_id + 1;
            END WHILE;
            
            SET hour_val = hour_val + 1;
        END WHILE;
        
        SET day_offset = day_offset - 1;
    END WHILE;
END //

DELIMITER ;

-- 调用存储过程生成数据
CALL GenerateTestRecords();

-- 删除存储过程
DROP PROCEDURE GenerateTestRecords;
